package com.java5;

public class RelactionalOperator {

	public static void main(String[] args) {
		int a=5,b=7;
		// value of a and b
	    System.out.println("a is " + a + " and b is " + b);

	    
	    // > operator
	    System.out.println(a > b);  // false

	    // operator
	    System.out.println(a < b);  // true

	    // >= operator
	    System.out.println(a >= b);  // false

	    // <= operator
	    System.out.println(a <= b);  // true
	}

}
